#include <bits/stdc++.h>

#define print_cont(arr)        \
    for (auto e : arr)         \
        std::cout << e << " "; \
    std::cout << std::endl;

#define print_map(arr)                                  \
    for (auto e : arr)                                  \
        std::cout << e.first << " " << e.second << " "; \
    std::cout << std::endl;